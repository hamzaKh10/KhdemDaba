export function getLang() {
  const raw = localStorage.getItem(STORAGE_KEY);
  if (raw === "ar") return "ar";
  if (raw === "dar") return "dar";
  return "en";
}

export function applyLangToDocument() {
  const lang = getLang();
  document.documentElement.lang = lang === "dar" ? "ar-MA" : lang;
  document.documentElement.dir = lang === "en" ? "ltr" : "rtl";
}

export function setLang(lang) {
  const v = lang === "ar" ? "ar" : lang === "dar" ? "dar" : "en";
  localStorage.setItem(STORAGE_KEY, v);
  applyLangToDocument();
  window.dispatchEvent(new Event("khdem:lang"));
}

export function toggleLang() {
  const cur = getLang();
  setLang(cur === "en" ? "ar" : cur === "ar" ? "dar" : "en");
}

export function t(key, vars) {
  const lang = getLang();
  let str = DICT[lang]?.[key];
  if (str == null && lang === "dar") str = DICT.ar?.[key];
  if (str == null) str = DICT.en?.[key];
  if (str == null) str = String(key);
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replaceAll(`{${k}}`, String(v));
    }
  }
  return str;
}
